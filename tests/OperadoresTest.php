<?php 

use PHPUnit\Framework\TestCase;
use function PHPUnit\Framework\assertEquals;

final class OperadoresTest extends TestCase
{
    public function testOperadoresAritmeticos(): void
    {
        $result = 7 + 6 * (4 - 2) - 10 ;
        assertEquals(9, $result, '¿Cuál es el resultado de la operación?');
    }

    public function testConcatenacion(): void
    {
        $name = 'Carlos';
        $lastNames = 'Herrera Conejero';
        // $result = "$lastNames, $name";
        $result = $lastNames.", ".$name;

        $expectedResult = 'Herrera Conejero, Carlos';
        assertEquals($expectedResult, $result);
    }

    public function testOperadoresBooleanos(): void
    {
        // Operadores lógicos
        assertEquals(true, 15 > 7, 'Mayor que');
        assertEquals(false, 15 < 7, 'Menor que');
        assertEquals(true, 15 >= 7, 'Mayor o igual que');
        assertEquals(false, 15 <= 7, 'Menor o igual que');
        assertEquals(true, 15 == 15, 'Igual que');
        assertEquals(true, 15 == '15', 'Igual que');
        assertEquals(true, 15 === 15, 'Estrictamente igual que');
        assertEquals(false, 15 === '15', 'Estrictamente igual que');

        // And, Or, Not
        assertEquals(false, true && false, 'And');
        assertEquals(true, true || false, 'Or');
        assertEquals(false, !true, 'Not');
    }

}